<?php

// Арифметичні оператори

$x = 3;
$y = 2;

echo $x + $y, PHP_EOL;   // 5 (сума)
echo $x - $y, PHP_EOL;   // 1 (різниця)
echo $x * $y, PHP_EOL;   // 6 (добуток)
echo $x / $y, PHP_EOL;   // 1.5 (частка)
echo $x % $y, PHP_EOL;   // 1 (залишок від цілісного поділу)
echo $x ** $y, PHP_EOL;  // 9 (зведення в ступінь)
echo +$x, PHP_EOL;       // 3 (конвертація у число)
echo -$x, PHP_EOL;       // -3 (зміна знаку)
